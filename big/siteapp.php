	<p>Today so many people use their smartphone as their primary way of connecting to the internet. Considering that, according to Statcounter, over 55% of the world uses their smartphones to go online, you probably do too. It might be tempting to ask yourself: Which provides the best online experience for a user: a responsive website or a mobile app? Or perhaps: Which way provides more opportunities for my target audience to view my product?</p>
	<p>A responsive website is a development technique. Using this technique a website can detect what size of screen a user is on and adjusts the layout accordingly. If your on a mobile phone the site will be displayed in a single column format as opposed to a three-column format on a desktop. On the same website, the same information just displayed differently allowing for better viewing ability.</p>
	<p>Typically a responsive website is cheaper to create and maintain. Search engines such as Google can display it in search results. Plus it can be accessed by most platforms (mobile, tablet, desktop).</p>
	<p>There are a few downsides to a responsive website too. An internet connection is a requirement. It’s also not as convenient because it’s meant to work with all devices it may not work 100% with one (codecontrol).
</p>
	<p>Mobile apps, also known as native apps, are designed to be used on a specific mobile operating system (OS). Typically either an Apple OS (iOS0 or Android OS.  That app must be downloaded and installed on a mobile device in order to be used.
</p>
	<p>Mobile apps allow for personalization and have better performance since it is built for specific OS. One of the biggest disadvantages is how expensive it is to build a mobile app much less two apps since iOS and Andriod aren’t compatible, plus maintain the app with updates.
</p>
	<p>How to know if you need a responsive site or a mobile app? Melanie Seibert summed it up best. Ask yourself this: Do I need access to...</p>
	<ul>
		<li>location / navigation</li>
		<li>camera</li>
		<li>microphone</li>
		<li>haptic feedback (that fun 'buzzy' vibration feeling the phone makes)</li>
		<li>directional sensors (like a compass or motion snesor)</li>
	</ul>
	<p>If you answered yes to one of the items listed above, a mobile app might be the way to go. However, if you answered no to all the items listed above perhaps a responsive website is the way. There is a chance you might want both a responsive site and a mobile app. Comes down to what you want, what your target audience wants, and how much you want to pay.</p>
</ul>
